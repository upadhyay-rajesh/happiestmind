class bit
{
	public static void main(String args[])
	{
		int i=10;
		int j=-10;
	
	System.out.println("left is :"+(i<<2));
	System.out.println("right is :"+(i>>2));
	System.out.println("unsigned is :"+(i>>>2));
System.out.println("Negative value of left is :"+(j<<2));
System.out.println("Negative value of right is :"+(j>>2));
System.out.println("Negative value of unsigned is :"+(j>>>2));

	}
}
